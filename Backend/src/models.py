# HELPER FUNCTIONS

def get_supported_models():
    return [
        (url, key, models.split(","))
        for url, key, models in zip(
            os.getenv("LLM_URLS", "openai").split(";"), 
            os.getenv("LLM_APIKEYS", "").split(";"), 
            os.getenv("LLM_MODELS", "gpt-4o-mini,gpt-4o").split(";"),
        )
    ]
